using System;
using System.Collections;
using System.Diagnostics.CodeAnalysis;
class Program
{
    static void Main()
    {
        int a = -10;
        double b = 3.14159;

        Math.Abs(a) // 절대값

        Math.Max(3, 7) // 큰 값 반환

        Math.Min(3, 7) // 작은 값 반환

        Math.Pow(2, 3) // 제곱 2의 3승

        Math.Sqrt(16) // 제곱근

        Math.Round(b, 2) // 반올림(소수점 2자리)

        Math.Floor(b) // 내림

        Math.Ceiling(b) // 올림




    }
}